-- ============================================================================
-- 状态栏美化: nvim-lualine/lualine.nvim
-- ============================================================================
-- 功能说明:
--   • 美化底部状态栏，显示编辑模式、Git 分支、文件名、LSP 状态、进度和光标位置
--   • 集成 nvim-web-devicons，为文件类型和状态栏组件提供图标支持
--   • 显示当前文件路径、修改状态、只读状态和新文件状态
--   • 显示当前 LSP 客户端状态，便于确认语言服务器是否正常工作
-- 配置效果:
--   ├─ 信息清晰: 左侧显示模式、分支和文件路径，右侧显示 LSP、进度和位置
--   ├─ 路径完整: 文件名区域显示带 ~ 的绝对路径，适合多项目开发
--   ├─ LSP 可见: 一眼确认当前 buffer 是否已挂载语言服务器
--   └─ 状态直观: 文件修改、只读、新文件状态直接显示在状态栏中

-- ============================================================================
-- 插件安装
-- ============================================================================

vim.pack.add({
  {
    src = "https://github.com/nvim-tree/nvim-web-devicons",
    name = "nvim-web-devicons",
  },
  {
    src = "https://github.com/nvim-lualine/lualine.nvim",
    name = "lualine",
  },
})

-- ============================================================================
-- 状态栏配置
-- ============================================================================
-- Lualine 默认分为 6 个区域:
--
--   +-------------------------------------------------+
--   | A | B | C                             X | Y | Z |
--   +-------------------------------------------------+
--
-- 常见布局:
--   • lualine_a: 编辑模式
--   • lualine_b: Git 分支
--   • lualine_c: 文件信息
--   • lualine_x: LSP / 编码 / 文件类型等扩展信息
--   • lualine_y: 文件进度
--   • lualine_z: 光标位置

require("lualine").setup({
  sections = {
    -- 左侧 a: 当前编辑模式
    -- 例如 NORMAL、INSERT、VISUAL 等
    lualine_a = {
      "mode",
    },

    -- 左侧 b: Git 分支
    -- 在 Git 仓库中显示当前所在分支
    lualine_b = {
      "branch",
    },

    -- 左侧 c: 当前文件信息
    -- 显示文件路径、修改状态、只读状态、新文件状态等
    lualine_c = {
      {
        "filename",

        -- 显示文件状态
        -- 包括 modified / readonly / newfile 等状态
        file_status = true,

        -- 文件路径显示方式:
        --   0: 只显示文件名
        --   1: 显示相对路径
        --   2: 显示绝对路径
        --   3: 显示绝对路径，并把 home 目录替换成 ~
        --   4: 显示文件名和父目录，并把 home 目录替换成 ~
        path = 3,

        symbols = {
          modified = "[+]",       -- 文件已修改但未保存
          readonly = "[-]",       -- 文件只读或不可修改
          unnamed = "[No Name]",  -- 未命名 buffer
          newfile = "[New]",      -- 新建文件，尚未第一次写入磁盘
        },
      },
    },

    -- 右侧 x: LSP 状态
    -- 显示当前 buffer 连接的 LSP 名称和运行状态
    lualine_x = {
      {
        "lsp_status",

        -- LSP 状态图标
        icon = "",

        symbols = {
          -- LSP 正在处理任务时显示的 spinner
          spinner = {
            "⠋",
            "⠙",
            "⠹",
            "⠸",
            "⠼",
            "⠴",
            "⠦",
            "⠧",
            "⠇",
            "⠏",
          },

          -- LSP 空闲或任务完成时显示
          done = "✓",

          -- 多个 LSP 名称之间的分隔符
          separator = " ",
        },

        -- 忽略指定 LSP 名称
        -- 例如不想显示 null-ls，可以写: { "null-ls" }
        ignore_lsp = {},

        -- 是否显示 LSP 名称
        show_name = true,
      },
    },

    -- 右侧 y: 文件滚动进度
    -- 例如 Top、Bot、50% 等
    lualine_y = {
      "progress",
    },

    -- 右侧 z: 光标位置
    -- 显示当前行号和列号
    lualine_z = {
      "location",
    },
  },
})