-- ============================================================================
-- Lazy.nvim 插件管理器配置
-- ============================================================================
--
-- 目录约定：
-- ~/.config/nvim/
-- ├── init.lua
-- └── lua/
--     ├── config/
--     │   └── lazy.lua
--     └── plugins/
--         ├── editor.lua
--         ├── lsp.lua
--         ├── completion.lua
--         ├── treesitter.lua
--         ├── git.lua
--         └── ui.lua
--

-- ============================================================================
-- 基础变量
-- ============================================================================

local lazypath = vim.fn.stdpath("data") .. "/lazy/lazy.nvim"
local lazyrepo = "https://github.com/folke/lazy.nvim.git"

-- ============================================================================
-- Bootstrap lazy.nvim
-- ============================================================================

if not (vim.uv or vim.loop).fs_stat(lazypath) then
    local out = vim.fn.system({
        "git",
        "clone",
        "--filter=blob:none",
        "--branch=stable",
        lazyrepo,
        lazypath,
    })

    if vim.v.shell_error ~= 0 then
        vim.api.nvim_echo({
            { "Failed to clone lazy.nvim:\n", "ErrorMsg" },
            { out, "WarningMsg" },
            { "\nPress any key to exit..." },
        }, true, {})

        vim.fn.getchar()
        os.exit(1)
    end
end

vim.opt.rtp:prepend(lazypath)

-- ============================================================================
-- Leader 键
-- ============================================================================

vim.g.mapleader = " "
vim.g.maplocalleader = "\\"

-- ============================================================================
-- Lazy.nvim 主配置
-- ============================================================================

require("lazy").setup({
    -- ========================================================================
    -- 插件入口
    -- ========================================================================
    spec = {
        { import = "plugins" },
    },

    -- ========================================================================
    -- 默认行为
    -- ========================================================================

    defaults = {
        -- false 表示默认不懒加载，除非插件自己配置 event/cmd/ft/keys
        lazy = false,

        -- 不默认使用 release tag
        version = false,
    },

    -- ========================================================================
    -- 安装配置
    -- ========================================================================

    install = {
        -- 首次安装插件时使用的临时 colorscheme
        colorscheme = { "habamax" },

        -- 缺失插件时自动安装
        missing = true,
    },

    -- ========================================================================
    -- 更新检查
    -- ========================================================================

    checker = {
        -- 建议先关闭，避免频繁弹通知
        -- 需要检查更新时手动执行 :Lazy check
        enabled = false,

        -- 即使开启 checker，也不弹通知
        notify = false,
    },

    -- ========================================================================
    -- 变更检测
    -- ========================================================================

    change_detection = {
        -- 修改插件配置后自动检测
        enabled = true,

        -- 不弹通知，避免干扰
        notify = false,
    },

    -- ========================================================================
    -- UI 配置
    -- ========================================================================

    ui = {
        border = "rounded",

        icons = {
            cmd = " ",
            config = " ",
            event = " ",
            ft = " ",
            init = " ",
            import = " ",
            keys = " ",
            lazy = "󰒲 ",
            loaded = "●",
            not_loaded = "○",
            plugin = " ",
            runtime = " ",
            source = " ",
            start = " ",
            task = "✔ ",
            list = {
                "●",
                "➜",
                "★",
                "‒",
            },
        },
    },

    -- ========================================================================
    -- 性能优化
    -- ========================================================================

    performance = {
        cache = {
            enabled = true,
        },

        rtp = {
            -- 禁用部分内置插件
            -- 这些插件大多数情况下用不上，关掉可以减少启动负担
            disabled_plugins = {
                "gzip",
                "matchit",
                "matchparen",
                "netrwPlugin",
                "tarPlugin",
                "tohtml",
                "tutor",
                "zipPlugin",
            },
        },
    },
})
