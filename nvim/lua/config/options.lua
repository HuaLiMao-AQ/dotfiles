-- ============================================================================
-- Neovim 编辑器基础选项配置
-- ============================================================================
-- 本文件定义 Neovim 的核心编辑行为，包括缩进、UI、搜索等

---@diagnostic disable: undefined-global
local opt = vim.opt

-- ============================================================================
-- 快捷键前缀设置
-- ============================================================================
vim.g.mapleader = " " -- 主快捷键前缀：Space
vim.g.maplocalleader = " " -- 本地快捷键前缀：Space

-- ============================================================================
-- 制表符和缩进配置
-- ============================================================================
opt.list = true -- 显示缩进参考线
opt.listchars = {
    space = "·",     -- 普通空格
    tab = "→ ",      -- Tab，建议后面带一个空格
    trail = "·",     -- 行尾多余空格
    extends = "»",   -- 行右侧还有内容
    precedes = "«",  -- 行左侧还有内容
    nbsp = "␣",      -- 不间断空格
}
opt.tabstop = 4 -- 制表符显示宽度：4 个空格
opt.softtabstop = 4 -- 插入/删除制表符时的空格数：4
opt.shiftwidth = 4 -- 自动缩进宽度：4 个空格
opt.expandtab = true -- 将制表符转换为空格
opt.autoindent = true -- 自动继承缩进
opt.smartindent = true -- 智能缩进

-- ============================================================================
-- UI 界面配置
-- ============================================================================
opt.number = true -- 显示绝对行号
opt.relativenumber = true -- 显示相对行号（相对于当前行）
opt.cursorline = true -- 高亮当前行
opt.splitbelow = true -- 水平分割窗口时，新窗口在下方
opt.splitright = true -- 垂直分割窗口时，新窗口在右方
opt.showmode = false -- 不在底部显示模式（由 lualine 显示）
opt.laststatus = 3 -- 全局状态栏（所有窗口共用一个状态栏）
opt.termguicolors = true -- 启用 24 位真彩色支持

-- ============================================================================
-- 搜索配置
-- ============================================================================
opt.incsearch = true -- 输入搜索词时实时显示搜索结果（增量搜索）
opt.hlsearch = false -- 搜索后不高亮匹配的文本
opt.ignorecase = true -- 搜索时忽略大小写
opt.smartcase = true -- 搜索包含大写字母时自动区分大小写
