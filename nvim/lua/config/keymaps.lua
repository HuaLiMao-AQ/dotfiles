-- ============================================================================
-- 快捷键映射配置
-- ============================================================================
-- 本文件定义 Neovim 的所有快捷键映射，包括窗口导航和调整

---@diagnostic disable: undefined-global

-- 快捷键选项：不递归映射 + 静默执行（不显示命令）
local opts = {
  noremap = true,   -- 不递归映射（避免映射中的映射）
  silent = true,    -- 静默执行（不在命令行显示）
}

-- ============================================================================
-- 普通模式快捷键
-- ============================================================================

-- 窗口导航: Ctrl + hjkl 在窗口之间移动
-- h: 左  j: 下  k: 上  l: 右
vim.keymap.set("n", "<C-h>", "<C-w>h", opts)  -- 移动到左窗口
vim.keymap.set("n", "<C-j>", "<C-w>j", opts)  -- 移动到下窗口
vim.keymap.set("n", "<C-k>", "<C-w>k", opts)  -- 移动到上窗口
vim.keymap.set("n", "<C-l>", "<C-w>l", opts)  -- 移动到右窗口

-- 窗口调整大小: Ctrl + 方向键
-- 每次调整 2 行或 2 列
vim.keymap.set("n", "<C-Up>", ":resize -2<CR>", opts)      -- 减小窗口高度
vim.keymap.set("n", "<C-Down>", ":resize +2<CR>", opts)    -- 增加窗口高度
vim.keymap.set("n", "<C-Left>", ":vertical resize -2<CR>", opts)   -- 减小窗口宽度
vim.keymap.set("n", "<C-Right>", ":vertical resize +2<CR>", opts)  -- 增加窗口宽度

-- ============================================================================
-- 可视模式快捷键
-- ============================================================================

-- 智能缩进: < 和 > 快捷键在可视模式下保持选择
-- 这样可以多次调整缩进，而不需要重新选择文本
vim.keymap.set("v", "<", "<gv", opts)  -- 左移并保持选择
vim.keymap.set("v", ">", ">gv", opts)  -- 右移并保持选择
