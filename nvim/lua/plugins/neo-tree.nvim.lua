-- ============================================================================
-- Neo-tree.nvim 文件树插件
-- ============================================================================
-- 功能说明:
--   Neo-tree 是一个现代化的文件树插件，适合作为常驻侧边栏使用。
--   相比 oil.nvim，Neo-tree 更接近 VSCode / JetBrains 的文件浏览器体验。
--
-- 配置效果:
--   1. 使用 <leader>l 打开/关闭右侧文件树
--   2. 使用 <leader>o 定位当前文件
--   3. 默认显示隐藏文件和 git ignored 文件
--   4. 文件树显示在右侧，宽度为 34
--   5. 自动跟随当前打开的文件
-- ============================================================================

return {
	"nvim-neo-tree/neo-tree.nvim",
	branch = "v3.x",

	dependencies = {
		"nvim-lua/plenary.nvim",
		"nvim-tree/nvim-web-devicons",
		"MunifTanjim/nui.nvim",
	},

	keys = {
		{
			"<leader>l",
			"<cmd>Neotree toggle left<CR>",
			desc = "打开/关闭 Neo-tree 文件树",
		},
		{
			"<leader>o",
			"<cmd>Neotree reveal left<CR>",
			desc = "在 Neo-tree 中定位当前文件",
		},
	},

	opts = {
		-- =========================================================================
		-- 文件系统配置
		-- =========================================================================
		filesystem = {
			-- 自动跟随当前文件
			follow_current_file = {
				enabled = true,
			},

			-- 文件过滤规则
			filtered_items = {
				visible = true, -- 显示被过滤的文件
				hide_dotfiles = false, -- 不隐藏 .env / .gitignore 等点文件
				hide_gitignored = false, -- 不隐藏 git ignored 文件
			},
		},

		-- =========================================================================
		-- 窗口配置
		-- =========================================================================
		window = {
			position = "right",
			width = 34,
		},

		-- =========================================================================
		-- 默认源配置
		-- =========================================================================
		source_selector = {
			winbar = false,
			statusline = false,
		},

		-- =========================================================================
		-- 渲染配置
		-- =========================================================================
		default_component_configs = {
			indent = {
				with_expanders = true,
				expander_collapsed = "",
				expander_expanded = "",
			},
		},
	},
}
