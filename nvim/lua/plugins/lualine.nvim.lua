-- ============================================================================
-- 状态栏美化: nvim-lualine/lualine.nvim
-- ============================================================================
--
-- 功能说明:
--   • 美化 Neovim 底部状态栏，显示模式、Git 分支、文件路径、LSP、文件类型等信息
--   • 使用 Tokyonight 主题，与当前 colorscheme 保持统一
--   • 使用全局状态栏，避免多窗口时每个窗口都显示一条状态栏
--   • 显示 Git diff 状态，快速查看当前文件新增、修改、删除情况
--   • 显示当前 buffer 挂载的 LSP，便于确认语言服务器是否正常工作
--
-- 配置效果:
--   ├─ 左侧: 编辑模式 / Git 分支 / Git diff
--   ├─ 中间: 当前文件路径、修改状态、只读状态、新文件状态
--   ├─ 右侧: LSP 状态 / 文件类型 / 文件进度 / 光标位置
--   └─ 风格: 克制、信息密度高，不使用过重的 powerline 分隔符
--
-- 预期效果:
--
--   NORMAL   dev  +12 ~3 -1  ~/Project/ShanCircle/server/main.go       gopls ✓  go  42%  120:8
--
-- Lazy.nvim 说明:
--   • dependencies 用于声明 mini.icons 图标依赖
--   • event = "VeryLazy" 表示启动完成后延迟加载
--   • opts 会自动传给 require("lualine").setup(...)
--

return {
    {
        "nvim-lualine/lualine.nvim",

        -- 启动完成后加载
        -- 状态栏不需要阻塞 Neovim 首屏启动
        event = "VeryLazy",

        -- 图标依赖
        -- lualine 的 filetype、branch 等组件会用到这些图标
        dependencies = {
            "nvim-mini/mini.icons",
        },

        opts = {
            -- ====================================================================
            -- 全局配置
            -- ====================================================================

            options = {
                -- 使用 Tokyonight 主题
                -- 如果当前 colorscheme 是 tokyonight，这里会保持颜色风格一致
                theme = "tokyonight",

                -- 启用全局状态栏
                -- 多窗口时只在底部显示一条状态栏，而不是每个窗口一条
                globalstatus = true,

                -- 组件分隔符
                component_separators = {
                    left = "",
                    right = "",
                },

                -- 区域分隔符
                section_separators = {
                    left = "",
                    right = "",
                },

                -- 始终保持中间区域分隔
                -- 避免左侧和右侧信息在窗口变窄时挤在一起
                always_divide_middle = true,

                -- 禁用特定文件类型的状态栏
                -- dashboard / alpha / starter 这类首页插件不需要显示完整状态栏
                disabled_filetypes = {
                    statusline = {
                        "dashboard",
                        "alpha",
                        "starter",
                    },
                    winbar = {},
                },
            },

            -- ====================================================================
            -- 状态栏区域配置
            -- ====================================================================
            --
            -- Lualine 默认分为 6 个区域:
            --
            --   +-------------------------------------------------+
            --   | A | B | C                             X | Y | Z |
            --   +-------------------------------------------------+
            --
            -- 当前布局:
            --   • lualine_a: 编辑模式
            --   • lualine_b: Git 分支 / Git diff
            --   • lualine_c: 当前文件路径
            --   • lualine_x: LSP 状态 / 文件类型
            --   • lualine_y: 文件进度
            --   • lualine_z: 光标位置

            sections = {
                -- ==================================================================
                -- 左侧 A 区域: 当前编辑模式
                -- ==================================================================
                --
                -- 显示 NORMAL / INSERT / VISUAL / COMMAND 等模式。
                -- 这里不缩写为 N / I / V，原因是完整模式名更直观。

                lualine_a = {
                    {
                        "mode",

                        -- 保留完整模式名
                        -- 如果想改成单字母，可以使用:
                        -- fmt = function(str)
                        --     return str:sub(1, 1)
                        -- end
                        fmt = function(str)
                            return str
                        end,
                    },
                },

                -- ==================================================================
                -- 左侧 B 区域: Git 信息
                -- ==================================================================
                --
                -- branch:
                --   显示当前 Git 分支
                --
                -- diff:
                --   显示当前文件相对 Git 的变更数量
                --   例如 +12 ~3 -1 表示新增 12 行、修改 3 行、删除 1 行

                lualine_b = {
                    {
                        "branch",
                        icon = "",
                    },

                    {
                        "diff",

                        -- Git diff 显示符号
                        -- 使用简单字符，避免状态栏过于复杂
                        symbols = {
                            added = "+",
                            modified = "~",
                            removed = "-",
                        },
                    },
                },

                -- ==================================================================
                -- 中间 C 区域: 当前文件信息
                -- ==================================================================
                --
                -- filename:
                --   显示当前 buffer 对应的文件路径
                --
                -- path 参数说明:
                --   0: 只显示文件名
                --   1: 显示相对路径
                --   2: 显示绝对路径
                --   3: 显示绝对路径，并把 home 目录替换成 ~
                --   4: 显示文件名和父目录，并把 home 目录替换成 ~
                --
                -- 这里使用 path = 3:
                --   适合你同时打开多个项目时确认当前文件位置。

                lualine_c = {
                    {
                        "filename",

                        -- 显示文件状态
                        -- 包括 modified / readonly 等状态
                        file_status = true,

                        -- 显示新文件状态
                        -- 新建但尚未写入磁盘的文件会显示 [New]
                        newfile_status = true,

                        -- 显示带 ~ 的绝对路径
                        path = 3,

                        symbols = {
                            modified = " [+]",      -- 文件已修改但未保存
                            readonly = " [-]",      -- 文件只读
                            unnamed = "[No Name]",  -- 未命名 buffer
                            newfile = " [New]",     -- 新文件
                        },
                    },
                },

                -- ==================================================================
                -- 右侧 X 区域: LSP 状态 / 文件类型
                -- ==================================================================
                --
                -- lsp_status:
                --   显示当前 buffer 挂载的 LSP 客户端
                --   例如 gopls / rust_analyzer / lua_ls / pyright
                --
                -- filetype:
                --   显示当前文件类型
                --   例如 go / rust / lua / python / markdown

                lualine_x = {
                    {
                        "lsp_status",

                        -- LSP 状态图标
                        icon = "",

                        symbols = {
                            -- LSP 正在处理任务时显示的 spinner
                            spinner = {
                                "⠋",
                                "⠙",
                                "⠹",
                                "⠸",
                                "⠼",
                                "⠴",
                                "⠦",
                                "⠧",
                                "⠇",
                                "⠏",
                            },

                            -- LSP 空闲或任务完成时显示
                            done = "✓",

                            -- 多个 LSP 名称之间的分隔符
                            separator = " ",
                        },

                        -- 忽略指定 LSP
                        -- 例如不想显示 null-ls，可以写:
                        -- ignore_lsp = { "null-ls" }
                        ignore_lsp = {},

                        -- 显示 LSP 名称
                        -- false 时只显示图标和状态，不显示具体 server 名
                        show_name = true,
                    },

                    {
                        "filetype",

                        -- 显示文件类型图标和文字
                        -- true  只显示图标
                        -- false 显示图标 + 文件类型名称
                        icon_only = false,
                    },
                },

                -- ==================================================================
                -- 右侧 Y 区域: 文件进度
                -- ==================================================================
                --
                -- 显示当前光标所在位置在整个文件中的百分比。
                -- 例如 Top / Bot / 42%。

                lualine_y = {
                    "progress",
                },

                -- ==================================================================
                -- 右侧 Z 区域: 光标位置
                -- ==================================================================
                --
                -- 显示当前行号和列号。
                -- 例如 120:8 表示第 120 行，第 8 列。

                lualine_z = {
                    "location",
                },
            },

            -- ====================================================================
            -- 非活动窗口状态栏
            -- ====================================================================
            --
            -- 因为启用了 globalstatus，非活动窗口的状态栏存在感较低。
            -- 这里保留一个简洁配置，避免多窗口时信息太乱。

            inactive_sections = {
                lualine_a = {},
                lualine_b = {},

                lualine_c = {
                    {
                        "filename",

                        -- 非活动窗口只显示相对路径
                        -- 避免状态栏过长
                        path = 1,

                        symbols = {
                            modified = " [+]",
                            readonly = " [-]",
                            unnamed = "[No Name]",
                            newfile = " [New]",
                        },
                    },
                },

                lualine_x = {
                    "location",
                },

                lualine_y = {},
                lualine_z = {},
            },

            -- ====================================================================
            -- 扩展支持
            -- ====================================================================
            --
            -- lazy:
            --   在 lazy.nvim 界面中使用更合适的状态栏
            --
            -- mason:
            --   在 Mason 界面中使用更合适的状态栏
            --
            -- quickfix:
            --   在 quickfix 窗口中使用更合适的状态栏

            extensions = {
                "lazy",
                "mason",
                "quickfix",
            },
        },
    },
}
